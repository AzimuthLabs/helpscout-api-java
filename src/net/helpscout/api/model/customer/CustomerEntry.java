package net.helpscout.api.model.customer;

public class CustomerEntry extends Object {
	private int id;
	private String value;
	private String type;
	private String location;
	
	public int getId() {
		return id;
	}
	public String getValue() {
		return value;
	}
	public String getType() {
		return type;
	}
	public String getLocation() {
		return location;
	}
}